// program to check taste of fruite using switch statement
package Easy;

import java.util.Scanner;

public class TasteofFruite {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in);
		System.out.println("Enter fruite name");
		String fruite = scan.next().trim();
		switch(fruite)
		{
		case "orange" : 
			System.out.println("Orange taste is acid");
			break;
		case "apple" :
			System.out.println("Apple taste is sweet");
			break;
		case "grapes" :
			System.out.println("Grapes taste is acid sweet");
			break;
		default :
			System.out.println("taste is good");
		}

	}

}
